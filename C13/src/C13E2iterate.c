//C13E2iterate.c        Demonstrates the concept of iteration
#include <stdio.h>

int main()
{
    int counter = 0;    // Initializing counter to numerical value of zero

    printf("counter = %d\n", counter = counter + 1); // counter now equal to 1
    printf("counter = %d\n", counter = counter + 1); // counter now equal to 1
    printf("counter = %d\n", counter = counter + 1); // counter now equal to 1

    return 0; 
}
