//C3E1int.c     Introduces the integer-type variable
#include <stdio.h>

int main()
{
    int snacks; // Declaring an integer-type variable called "snacks".

    snacks = 24; // Storing a numerical value of 24 into snacks
    printf("Burt has %d cookies.\n", snacks); // Snacks now expresses a value of 24
    
    return 0;
}
